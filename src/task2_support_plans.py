from fractions import Fraction
from itertools import combinations

# Метод: Гаусса–Жордана
# A — квадратная матрица n×n
# b — вектор правых частей длины n
# Возвращает:
# x — решение системы (вектор длины n), элементы типа Fraction
def solve(A, b):
    # Размер квадратной матрицы
    n = len(A)

    # Формируем расширенную матрицу aug = [A | b]
    aug = []
    i = 0
    while i < n:
        aug.append(A[i][:] + [b[i]])
        i += 1

    # r — текущая строка, c — текущий столбец (обрабатываем по диагонали)
    r = 0
    c = 0

    # Основной цикл приведения к ступенчатому виду
    while r < n and c < n:

        # Ищем ведущий элемент (pivot) в столбце c начиная со строки r
        pivot = r
        while pivot < n and aug[pivot][c] == 0:
            pivot += 1

        # Если pivot найден (не вышли за границы матрицы)
        if pivot < n:
            # Переставляем строки так, чтобы ведущий элемент оказался на строке r
            aug[r], aug[pivot] = aug[pivot], aug[r]

            # Нормируем ведущую строку (делаем ведущий элемент равным 1)
            div = aug[r][c]
            j = c
            while j <= n:
                aug[r][j] /= div
                j += 1

            # Обнуляем элементы в текущем столбце во всех остальных строках
            i = 0
            while i < n:
                if i != r:
                    factor = aug[i][c]
                    j = c
                    while j <= n:
                        aug[i][j] -= factor * aug[r][j]
                        j += 1
                i += 1

            # Переходим к следующей строке (следующему ведущему элементу)
            r += 1

        # Переходим к следующему столбцу
        c += 1

    # После преобразований решение находится в последнем столбце расширенной матрицы
    x = [Fraction(0)] * n
    i = 0
    while i < n:
        x[i] = aug[i][n]
        i += 1

    return x



# Исходные данные (вариант 8)
# A — матрица коэффициентов
# b — правая часть
A = [
    [6, -2, 2, 0, 5],
    [-1, -13, 7, 11, 0],
    [4, -7, 2, 1, 3],
    [5, -15, 9, 11, 5]
]

b = [2, 17, -6, 19]

# m — ранг системы (число базисных переменных)
# n — число переменных
m = 3
n = 5


# Коэффициенты целевой функции
# F = x1 + 2x2 + x3 + x4 + x5 -> max
c = [1, 2, 1, 1, 1]

print("\nВозможные базисные решения:\n")

# bestZ — текущее максимальное значение целевой функции
# bestX — соответствующий оптимальный план
bestZ = None
bestX = None

# Перебор всех возможных базисов размера m
# basis — набор индексов переменных, которые считаются базисными
for basis in combinations(range(n), m):

    # Формируем базисную матрицу Ab (берём m столбцов A по индексам basis)
    Ab = []
    i = 0
    while i < m:
        row = []
        j = 0
        while j < m:
            row.append(Fraction(A[i][basis[j]]))
            j += 1
        Ab.append(row)
        i += 1

    # Формируем базисную правую часть (первые m уравнений)
    bb = [Fraction(b[i]) for i in range(m)]

    # Решаем систему Ab * xbasic = bb
    xbasic = solve(Ab, bb)

    # Восстанавливаем полный вектор x размера n:
    # небазисные переменные = 0, базисные = значения из xbasic
    x = [Fraction(0)] * n

    j = 0
    while j < m:
        x[basis[j]] = xbasic[j]
        j += 1

    # Проверка допустимости (опорный план):
    # все координаты решения должны быть неотрицательны (x >= 0)
    feasible = True
    k = 0
    while k < n:
        if x[k] < 0:
            feasible = False
        k += 1

    # Если решение допустимое — вычисляем целевую функцию и сравниваем
    if feasible:

        # Вычисление значения целевой функции
        Z = Fraction(0)
        i = 0
        while i < n:
            Z += c[i] * x[i]
            i += 1

        # Вывод текущего базиса, решения и значения целевой функции
        print("Базис:", basis, "x =", x, "Z =", Z)

        # Поиск максимума целевой функции
        if bestZ is None or Z > bestZ:
            bestZ = Z
            bestX = x


# Вывод оптимального результата
print("\nОптимальный опорный план:")
print(bestX)
print("Z =", bestZ)