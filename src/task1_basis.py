from fractions import Fraction
from itertools import combinations

# Метод Гаусса–Жордана (RREF).
# Приводит расширенную матрицу (коэффициенты + правые части)
# к приведённому ступенчатому виду.
# Возвращает:
#   A      — преобразованную матрицу
#   pivots — список индексов опорных (ведущих) столбцов
def rref(matrix):
    # Создаём копию матрицы, чтобы не менять исходные данные
    A = [row[:] for row in matrix]

    # rows — число строк (уравнений), cols — число столбцов (переменных + 1 столбец b)
    rows = len(A)
    cols = len(A[0])

    # r — текущая строка (куда ставим ведущий элемент)
    # c — текущий столбец (в котором ищем ведущий элемент)
    r = 0
    c = 0

    # pivots хранит номера столбцов, где были найдены ведущие элементы
    pivots = []

    # Главный цикл: пока не вышли за строки и не дошли до последнего столбца (b)
    while r < rows and c < cols - 1:

        # pivot — индекс строки, где найден ненулевой элемент в столбце c
        pivot = -1

        # Ищем ненулевой элемент в столбце c, начиная со строки r
        i = r
        while i < rows:
            if A[i][c] != 0:
                pivot = i
            i += 1

        # Если в этом столбце не нашли ведущий элемент — переходим к следующему столбцу
        if pivot == -1:
            c += 1
        else:
            # Переставляем строки так, чтобы ведущий элемент оказался на строке r
            A[r], A[pivot] = A[pivot], A[r]

            # Нормируем ведущую строку: делим всю строку на ведущий элемент
            div = A[r][c]
            j = c
            while j < cols:
                A[r][j] /= div
                j += 1

            # Обнуляем все остальные элементы в текущем столбце c
            i = 0
            while i < rows:
                if i != r:
                    factor = A[i][c]
                    j = c
                    while j < cols:
                        A[i][j] -= factor * A[r][j]
                        j += 1
                i += 1

            # Запоминаем столбец, где получилась ведущая единица
            pivots.append(c)

            # Переходим к следующей строке и следующему столбцу
            r += 1
            c += 1

    # Возвращаем преобразованную матрицу и список ведущих столбцов
    return A, pivots



# Исходные данные (вариант 8)
# A — матрица коэффициентов, b — вектор правых частей
A = [
    [6, -2, 2, 0, 5],
    [-1, -13, 7, 11, 0],
    [4, -7, 2, 1, 3],
    [5, -15, 9, 11, 5]
]

b = [2, 17, -6, 19]


# Перевод всех элементов A и b в рациональные дроби Fraction
# (для точных вычислений без ошибок округления)
for i in range(len(A)):
    for j in range(len(A[0])):
        A[i][j] = Fraction(A[i][j])
    b[i] = Fraction(b[i])


# Формируем расширенную матрицу aug = [A | b]
# (к каждой строке A дописываем соответствующий элемент b)
aug = [A[i] + [b[i]] for i in range(len(A))]


# Приводим расширенную матрицу к RREF и получаем список ведущих столбцов
R, piv = rref(aug)

# Ранг системы = количество ведущих столбцов
rank = len(piv)


# Вывод матрицы и ранга
print("([A|b]):\n")
for row in R:
    print(row)

print("\nРанг =", rank)


# Перебор всех базисов размера rank (вывод всех базисных видов)
print("\nВсе базисные виды:\n")

for basis in combinations(range(5), rank):
    print("Базисные столбцы:", basis)